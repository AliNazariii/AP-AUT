public class Point
{
    //fields
    public int x;
    public int y;

    //constructors
    public Point()
    {
        x = 0;
        y = 0;
    }

    //methods
    public void setNew(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public Point getPoint(Point a)
    {
        return a;
    }
}
